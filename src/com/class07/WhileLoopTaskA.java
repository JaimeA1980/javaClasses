package com.class07;

public class WhileLoopTaskA {
		public static void main(String[] args) {
			//Print only odd number from 50 to 100
			//print only even numbers from 100 to 1
			
			int a=51;
			while (a<=100) {
				System.out.println(a);
				a+=2;
			}
			System.out.println("-------------------------------------------------------------------------------------------------------------------------------------------------------------");
			int b=100;
			
			while(b>=0) {
				System.out.println(b);
				b-=2;
			}
		}
	
	
}
