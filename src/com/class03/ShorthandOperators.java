package com.class03;

public class ShorthandOperators {
	
	/**
	 * @param args
	 */
	/**
	 * @param args
	 */
	/**
	 * @param args
	 */
	public static void main(String[] args) {
		
		
		int num=1000;

		num=num+1000;

		System.out.println(num);//would be 2000

		int num2=10;
		num2=num2-5;
		//at print it would be 5

		int num3=1000;
		num3+=1000;//num3=num3+1000;

		int num4=50;
		num4+=20;//num4=num4+20;
		System.out.println(num4);//70
		//shortcut for printing syso+ctrl+space
		
		num4-=30;
		System.out.println(num4);
		
		num4/=10;//40/10=4
		System.out.println(num4);
		
		num4%=2;//num4=4%2;
		System.out.println(num4);
		
		
				
		
	}

}
