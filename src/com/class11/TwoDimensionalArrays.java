package com.class11;

public class TwoDimensionalArrays {
	public static void main(String[] args) {
		//Declare 2D array
		
		
		String[][]arr= {
				
				{"Amaris","Noah","David"},
				{"Jaime","Celeste","Andrew"}
							
		};
		
		System.out.println(arr[1][1]);
		
		//to identify the number of rows
		System.out.println("The numbers of rows are: "+arr.length);
		
		//to identify the number of columns
		System.out.println("The number of columns: "+arr[1].length);
	
	System.out.println("_____________________________________________________________________________________________________________________________________________________________________");
		
}
}
